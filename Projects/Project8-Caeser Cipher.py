# TWO TIMES BCS IF WE WRITE THE LETTER Z THEN IT WILL GO FORWARD TO A TO Z 
alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n")) 

# ENCRYPTION
def encrypt(msg, amt): # msg=plain text, amt = shift amount
    cipher_text = ""
    for i in msg:      # i is for the letter
        a = alphabet.index(i) # a is the position
        position = a + amt    # position is the new position
        letter = alphabet[position] # letter is for the new letter
        cipher_text += letter 
    print(f"The encoded text is '{cipher_text}'")

# DECRYPTION 
def decrypt(cipher_txt, shift_amount): # txt=cipher_text, sft=shift_amount
    plain_text = ""
    for letter in cipher_txt:
        position = alphabet.index(letter)
        new_position = position - shift_amount
        plain_text += alphabet[new_position]
    print(f"The decoded text is '{plain_text}'")

if direction=="encode":
    encrypt(msg=text, amt=shift)
elif direction=="decode":
    decrypt(cipher_txt=text, shift_amount=shift)

