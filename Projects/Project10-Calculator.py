def add(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {"+" : add, "-" : sub, "*" : multiply, "/" : divide}

def calculator():
    num1 = float(input("Enter the first number: "))
    #num2 = int(input("Enter the second number: "))
    for symbol in operations:
        print(symbol)
    should_continue = True
    while should_continue:
        op = input("Which operation do you want to perform?\n")
        num2 = float(input("Enter the next number: "))
        calculation = operations[op]
        answer = calculation(num1, num2)
        print(f"{num1} {op} {num2} = {answer}")

        if input(f"Type 'Y' to continue with {answer} or type 'N' to start a new calculation\n") == "Y":
            num1 = answer
        else:
            should_continue = False
            #some condition to be met
            calculator()   #RECURSION
calculator()